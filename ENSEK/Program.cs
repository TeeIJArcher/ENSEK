using System.Reflection;
using Microsoft.OpenApi.Models;
using Models;
using Services;

var builder = WebApplication.CreateBuilder(args);

builder.Services.AddControllers();

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen(options =>
{
    options.SwaggerDoc("v1", new OpenApiInfo
    {
        Version = "v1.0.0",
        Title = "Ensek API",
        Description = "An ASP.NET Core Web API for an Energy Company Account Manager, able to load a CSV file of Customer Meter Readings so that monitoring of their energy consumption and charge can occure.",
    });

    var xmlFilename = $"{Assembly.GetExecutingAssembly().GetName().Name}.xml";
    options.IncludeXmlComments(Path.Combine(AppContext.BaseDirectory, xmlFilename));
});

builder.Services.Configure<EnsekDatabaseSettings>(
    builder.Configuration.GetSection("EnsekDatabase"));

builder.Services.AddSingleton<IEnsekDbService, EnsekDbService>();
builder.Services.AddScoped<ICSVService, CSVService>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

